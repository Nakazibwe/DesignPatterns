// Literals in Javascript.
// Most Types in JS can be created in 3 different ways.

// Strings.
const name1 = 'Grace';// String created Literally.
const name2 = String('Grace'); // A String created using a string factory.
const name3 = new String('Grace');// A string object constructor.

// Numbers.
let age = 120;// Number created Literally.
let age2 = Number(121) // A number created using a Number Factory.
let age3 = new Number(122) // Number Object Constructor.

// Booleans.
let conditionTest1 = true; // Boolean created literally
let conditionTest2  = Boolean(false) // A Boolean created using a Boolean Factory.
let conditionTest3 = new Boolean(true) // Boolean Object Constructor. 
